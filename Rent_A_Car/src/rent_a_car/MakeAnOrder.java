    public void Show_Orders_In_Table()
    {
        ArrayList<Order> list = getOrderList();
        DefaultTableModel model = (DefaultTableModel)Order_JTable.getModel();
        
        //clear jtable
        model.setRowCount(0);
        
        Object[] row = new Object[7];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getOrder_id();
            row[1] = list.get(i).getBrand_ordered();
            row[2] = list.get(i).getQuantity();
            row[3] = list.get(i).getSupplier_phone();
            row[4] = list.get(i).getSupplier_id();
            row[5] = list.get(i).getEmployee_id();
            row[6] = list.get(i).getCar_model();
            
            model.addRow(row); 
        }
        
    }
    
