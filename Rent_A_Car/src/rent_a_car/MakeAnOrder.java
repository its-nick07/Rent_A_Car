    public ArrayList<Order> getOrderList()
    {
        
        ArrayList<Order> orderList = new ArrayList<Order>();
        String sql = "SELECT * from orders";
        
        Statement st;
        ResultSet rs;
        
        try{
        
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                Order order;
                
                while(rs.next()){
                    
                    order = new Order(rs.getInt("order_id"),rs.getString("brand_ordered"),rs.getInt("quantity"),rs.getInt("supplier_phone"),rs.getInt("supplier_id"),rs.getInt("employee_id"),rs.getString("car_model"));
                    orderList.add(order);
                            
                }
                
           }
        
        catch(SQLException ex){
            
            JOptionPane.showMessageDialog(null, "Error" + ex);
            
        }
        return orderList;
        
        
        
    }
    
