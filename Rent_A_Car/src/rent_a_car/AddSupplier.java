    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed

        //Inserting Data Into The Database

        String sql = "INSERT INTO supplier"
        + "(supplier_id,supplier_name,supplier_contact,supplier_address)"
        +"VALUES (?,?,?,?)";

        try{

            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setString(1, txt_ID.getText());
            ps.setString(2, txt_SupplierName.getText());
            ps.setString(3, txt_SupplierContact.getText());
            ps.setString(4, txt_SupplierAddress.getText());
            

            ps.executeUpdate();
            Show_Supplier_In_Table();

            JOptionPane.showMessageDialog(null, "Record was successfully added");

            txt_ID.setText("");
            txt_SupplierName.setText("");
            txt_SupplierContact.setText("");
            txt_SupplierAddress.setText("");
            

            txt_ID.requestFocus();

        }
        catch(SQLException exc){

            JOptionPane.showMessageDialog(null, exc);

        }
    }//GEN-LAST:event_jButton10ActionPerformed

