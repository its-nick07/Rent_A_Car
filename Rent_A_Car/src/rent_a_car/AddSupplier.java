    public void Show_Supplier_In_Table()
    {
        ArrayList<Supplier> list = getSupplierList();
        DefaultTableModel model = (DefaultTableModel)Supplier_JTable.getModel();
        
        //clear jtable
        model.setRowCount(0);
        
        Object[] row = new Object[4];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getSupplier_id();
            row[1] = list.get(i).getSupplier_name();
            row[2] = list.get(i).getSupplier_contact();
            row[3] = list.get(i).getSupplier_address();
           
            
            model.addRow(row); 
        }
        
    }
    
