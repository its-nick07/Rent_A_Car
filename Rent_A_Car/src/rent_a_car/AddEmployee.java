    public void Show_Customers_In_Table()
    {
        ArrayList<Employee> list = getEmployeeList();
        DefaultTableModel model = (DefaultTableModel)Employee_JTable.getModel();
        
        //clear jtable
        model.setRowCount(0);
        
        Object[] row = new Object[4];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getEmployee_id();
            row[1] = list.get(i).getEmployee_name();
            row[2] = list.get(i).getContact();
            row[3] = list.get(i).getDesignation();
            
            
            model.addRow(row); 
        }
        
    }
    
