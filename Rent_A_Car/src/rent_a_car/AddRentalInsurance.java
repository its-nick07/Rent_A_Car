    public void Show_Insurance_In_Table()
    {
        ArrayList<RentalInsurance> list = getInsuranceList();
        DefaultTableModel model = (DefaultTableModel)RentalInsurance_JTable.getModel();
        
        //clear jtable
        model.setRowCount(0);
        
        Object[] row = new Object[6];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getBroker_id();
            row[1] = list.get(i).getBroker_name();
            row[2] = list.get(i).getDescription();
            row[3] = list.get(i).getPolicy();
            row[4] = list.get(i).getRental_id();
            row[5] = list.get(i).getBroker_id();
            
            
            model.addRow(row); 
        }
        
    }
    
