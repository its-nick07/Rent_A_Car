    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        //Inserting Data Into The Database

        String sql = "INSERT INTO rental_insurance"
        + "(insurance_id,broker_name,description,policy,rental_id,broker_id)"
        +"VALUES (?,?,?,?,?,?)";

        try{

            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setString(1, txt_InsuranceID.getText());
            ps.setString(2, txt_BrokerName.getText());
            ps.setString(3, txt_Description.getText());
            ps.setString(4, txt_Policy.getText());
            ps.setString(5, txt_RentalID.getText());
            ps.setString(6, txt_BrokerID.getText());

            ps.executeUpdate();
            Show_Insurance_In_Table();

            JOptionPane.showMessageDialog(null, "Record was successfully added");

            txt_InsuranceID.setText("");
            txt_BrokerName.setText("");
            txt_Description.setText("");
            txt_Policy.setText("");
            txt_RentalID.setText("");
            txt_BrokerID.setText("");

            txt_InsuranceID.requestFocus();

        }
        catch(SQLException exc){

            JOptionPane.showMessageDialog(null, exc);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

