    public ArrayList<RentalInsurance> getInsuranceList()
    {
        
        ArrayList<RentalInsurance> insuranceList = new ArrayList<RentalInsurance>();
        String sql = "SELECT * from rental_insurance";
        
        Statement st;
        ResultSet rs;
        
        try{
        
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                RentalInsurance rentalinsurance ;
                
                while(rs.next()){
                    
                    rentalinsurance = new RentalInsurance(rs.getInt("insurance_id"),rs.getString("broker_name"),rs.getString("description"),rs.getString("policy"),rs.getInt("rental_id"),rs.getInt("broker_id"));
                    insuranceList.add(rentalinsurance);
                            
                }
                
           }
        
        catch(SQLException ex){
            
            JOptionPane.showMessageDialog(null, "Error" + ex);
            
        }
        return insuranceList;
        
        
        
    }
    
