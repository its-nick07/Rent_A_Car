     public ArrayList<EmployeeDetails> getEmployeeDetailsList()
    {
        
        ArrayList<EmployeeDetails> employeedetailsList = new ArrayList<EmployeeDetails>();
        String sql = "SELECT * from employee_details";
        
        Statement st;
        ResultSet rs;
        
        try{
        
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                EmployeeDetails employeedetails;
                
                while(rs.next()){
                    
                    employeedetails = new EmployeeDetails(rs.getString("username"),rs.getString("password"));
                    employeedetailsList.add(employeedetails);
                            
                }
                
           }
        
        catch(SQLException ex){
            
            JOptionPane.showMessageDialog(null, "Error" + ex);
            
        }
        return employeedetailsList;
        
        
        
    }
    
