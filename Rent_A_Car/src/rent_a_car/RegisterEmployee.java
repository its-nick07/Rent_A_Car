    public void Show_EmployeeDetails_In_Table()
    {
        ArrayList<EmployeeDetails> list = getEmployeeDetailsList();
        DefaultTableModel model = (DefaultTableModel)EmployeeDetails_JTable.getModel();
        
        //clear jtable
        model.setRowCount(0);
        
        Object[] row = new Object[2];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getUsername();
            row[1] = list.get(i).getPassword();
            
            
            
            model.addRow(row); 
        }
        
    }
    
    public void ShowItem(int index)
    {
        txt_EmployeeID.setText((getEmployeeDetailsList().get(index).getUsername()));
        txt_EmployeeName.setText((getEmployeeDetailsList().get(index).getPassword()));
        
        
       
        
    }
    

