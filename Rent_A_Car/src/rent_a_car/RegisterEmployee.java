    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        //Inserting Data Into The Database

        String sql = "INSERT INTO employee_details"
        + "(username,password)"
        +"VALUES (?,?)";

        try{

            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setString(1, txt_EmployeeID.getText());
            ps.setString(2, txt_EmployeeName.getText());
        

            ps.executeUpdate();
            Show_EmployeeDetails_In_Table();

            JOptionPane.showMessageDialog(null, "Record was successfully added");

            txt_EmployeeID.setText("");
            txt_EmployeeName.setText("");
            

            txt_EmployeeID.requestFocus();

        }
        catch(SQLException exc){

            JOptionPane.showMessageDialog(null, exc);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

