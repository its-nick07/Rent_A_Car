    public ArrayList<Biilling> getBillingList()
    {
        
        ArrayList<Biilling> billingList = new ArrayList<Biilling>();
        String sql = "SELECT * from billing";
        
        Statement st;
        ResultSet rs;
        
        try{
        
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                Biilling billing;
                
                while(rs.next()){
                    
                    billing = new Biilling(rs.getInt("bill_id"),rs.getInt("bill_amount"),rs.getInt("customer_id"),rs.getInt("late_penalty"),rs.getInt("damage_penalty"));
                    billingList.add(billing);
                            
                }
                
           }
        
        catch(SQLException ex){
            
            JOptionPane.showMessageDialog(null, "Error" + ex);
            
        }
        return billingList;
        
        
        
    }
    
