    public void Show_Billing_In_Table()
    {
        ArrayList<Biilling> list = getBillingList();
        DefaultTableModel model = (DefaultTableModel)Billing_JTable.getModel();
        
        //clear jtable
        model.setRowCount(0);
        
        Object[] row = new Object[5];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getBill_id();
            row[1] = list.get(i).getBill_amount();
            row[2] = list.get(i).getCustomer_id();
            row[3] = list.get(i).getLate_penalty();
            row[4] = list.get(i).getDamage_penalty();
            
            
            
            model.addRow(row); 
        }
        
    }
    
